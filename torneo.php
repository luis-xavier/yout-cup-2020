            <p>Se seguirán las Reglas de FIFA</p>
            <p><strong class="strong-black">Formato:</strong> Futbol 7</p>
            <p><strong class="strong-black">Duración de los partidos:</strong> 2 tiempos de 20 minutos cada uno con 5 minutos de descanso y cambio de cancha</p>
            <p><strong class="strong-black">Mínimo de partidos a jugar por equipo:</strong> 3</p>
            <p>Un juego esta “completo” cuando al menos uno de los tiempos se haya terminado en caso de cancelación.</p>
            <p><strong class="strong-black">Equipamiento de los jugadores:</strong> Será a discreción del árbitro determinar la seguridad y la conveniencia del equipamiento de cada jugador incluyendo las espinilleras y zapatos.</p>
