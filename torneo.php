            <p>La ronda de K.O. (cuartos de final, semifinales y final) se jugarán a un partido, y en caso de haber empate en tiempo regular se procederá a desempate por medio de penales, calificando el ganador después de 3 disparos de manera alternada entre ambos equipos o de muerte súbita en caso de permanecer el empate.</p>
            <p>Después de las semifinales, habrá partido por el tercer y cuarto lugar. Si alguno de los equipos calificados a la Final Nacional no confirma su participación en la misma, se invitará a tomar su lugar a estos equipos en ese orden.</p>
