    <h2>La FC Bayern Youth Cup  llega a México</h2>
    <blockquote class="twitter-tweet"><p lang="es" dir="ltr">ℹ <a href="https://twitter.com/hashtag/FCBayern?src=hash&amp;ref_src=twsrc%5Etfw">#FCBayern</a> y Curveball Sports unen fuerzas para lanzar el prestigioso torneo juvenil, FC Bayern Youth Cup, en México. <br><br>➡ <a href="https://t.co/fFkzCeCftI">https://t.co/fFkzCeCftI</a> <a href="https://t.co/IpuiZVA6Ni">pic.twitter.com/IpuiZVA6Ni</a></p>&mdash; FC Bayern München Español (@FCBayernES) <a href="https://twitter.com/FCBayernES/status/1330888899560923149?ref_src=twsrc%5Etfw">November 23, 2020</a></blockquote> <script async src="https://platform.twitter.com/widgets.js" charset="utf-8"></script>
    <p><span class="letter-capital">F</span>C Bayern y Curveball Sports se han unido para llevar a cabo en México el prestigioso torneo juvenil, la FC Bayern Youth Cup, por primera ocasión en este país en el 2021.</p>
    <p>En un comunicado publicado en las redes oficiales del FC Bayern Munich, el reconocido
periodista Fernando Palomo conversó sobre la llegada del torneo con Rudolph Vidal,
presidente de Bayern Munich en las Américas, y Alan Achar, Director Ejecutivo de Curveball
Sports.</p>
    <p>“¿Qué es la Youth Cup y qué oportunidades le brinda a la juventud del país donde se juega?”
Vidal explicó: “es la competición para jóvenes más grande y más antigua de una entidad
internacional tan grande como lo es el Bayern. Empezó en 2012 y hemos tenido alrededor de
100,000 jugadores que han venido de China, Corea del Sur, Togo, Estados Unidos, Nigeria”.</p>
	<p>“Lo que hacemos es darle la oportunidad a jóvenes de 14 a 16 años la posibilidad
de demostrar su talento y de probar su habilidad para ser escogidos para formar
parte del equipo que viaja a Munich para competir en el Allianz Arena e
idealmente desarrollar su carrera en el futbol”.</p>
<p>Sobre cuál es el objetivo de Curveball Sports con el Bayern Munich, “lo más importante es
poder acercar a los aficionados al futbol mexicano al Bayern Munich”, comentó Alan Achar,
quien añadió “esta copa nos juntará, y esperamos que en los próximos años podamos traer
campamentos de futbol en los próximos veranos”.</p>
<p>Rudolph Vidal también resaltó la importancia que el club alemán le da a sus aficionados
mexicanos: “Uno de nuestros clubes de aficionados más grandes fuera de Alemania está en
México, con más de 4,000 aficionados”</p>
<p>“Lo que queremos hacer es brindar oportunidades futbolísticas en el país”</p>
<p>También se le preguntó a Vidal si “al formar parte de este torneo, ¿estos jóvenes tienen la
oportunidad de aprender a jugar el modo Bayern?”, a lo que enfatizó que “definitivamente van a
aprender mientras participan en el torneo. Queremos enseñarles a desarrollar habilidades que
puedan utilizar, como motivación, confianza, trabajo en equipo y disciplina”.</p>
<p>Sobre el futuro de la relación entre Curveball y FC Bayern Munich, Vidal destacó que “es muy
importante para nosotros y estamos orgullosos de trabajar con Curveball Sports en México y
estamos contentos de poder dar estas oportunidades a jóvenes mexicanos antes de la Copa
del Mundo 2026”.</p>

